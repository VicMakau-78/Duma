# if-elif-else statements
marks = 64

# if condition
if marks >=0 and marks <= 30:
    print("You Failed")
# elif condition 1
elif marks >= 31 and marks <= 50:
    print("Below average")  
# elif condition 2
elif marks >= 51 and marks <=70:
    print("Average")
# elif condition 3
elif marks >= 71 and marks <= 90:
    print("Above Average")
# elif condition 4
elif marks >= 91 and marks <= 100:
    print("Excellent")
# else
else:
    print("invalid marks")


