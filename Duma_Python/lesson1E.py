# Arithmetic operators
num1 = 78
num2 = 12

print("The first number is:", num1)
print("The second number is:", num2)


# addition
addition = num1 + num2
print("The addition of the two numbers is:", addition)


# subtraction
subtraction = num1 - num2
print("The subtraction of the two numbers is:", subtraction)


# multiplication
multiplication = num1 * num2
print("The multiplication of the two numbers is:", multiplication)


# division
division = num1 / num2
print("The division of the two numbers is:", division)


# modulus
modulus = num1 % num2
print("The modulus of the two numbers is:", modulus)


# Exponent
exponent = num1 ** num2
print("The exponent of the two numbers is:", exponent)


# Integer division
int_division = num1 // num2
print("The integer division of the two numbers is:", int_division)

# area  of a circle
pie = 3.142
r = 21

area = pie * r ** 2
print("The area of the circle is:", area)

