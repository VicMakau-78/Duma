// if-else statement is a conditional statement that allows us to execute a block of code if a specified condition is true, and another block of code if the condition is false.

let age = 12
if (age < 18) {
    console.log("You are too young to watch this movie.") // Output: You are too young to watch this movie.
} else {
    console.log("You can watch this movie.") // Output: You can watch this movie.
}