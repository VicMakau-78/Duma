// In javascript, we need to have initializers before declaring a varible. In ES6 we have two main types of intitialisers i.e:

// let: it declares values which can be changed later on in the program. It is used when we want to reassign a value to a variable. For example:


let weight = 70 // This variable can be reassigned later on 

console.log(weight); // Output: 70

// const: it i used to declre variables which contain values that are not intended to be changed. It is used when we want to declare a constant value that should not be reassigned. For example:

const pi = 3.142 // This variable cannot be reassigned
console.log("The value of pi is: ", pi); // Output: 3.142
